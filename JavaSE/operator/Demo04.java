package JavaSE.operator;
//逻辑运算符
public class Demo04 {
    public static void main(String[] args) {
        //与and   或or   非not
        boolean a = true;
        boolean b = false;
        System.out.println("a&&b:"+(a&&b));
        System.out.println("a||b:"+(a||b));
        System.out.println("!a:"+(!a));
    }


}
