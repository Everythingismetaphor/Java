package JavaSE.base;

public class Demo02
{
    public static void main(String[] args) {
        // java是强类型语言：要求变量的使用要严格符合规定，所有变量都必须先定义后才能使用
        // 八大基本数据类型
        // 整型
        int nmu1 = 10;
        byte nmu2 = 20;
        short num3 = 30;
        long num4 = 30L; // Long类型要在数字后面加L

        //小数：浮点数
        float num5 =50.1F;
        double num6 = 3.1415926535; //Float类型要在数字后面加F


        //字符  只能有一个字符(单引号)
        char name = 'A';
        //字符串，String不是关键字，是一个类(双引号)
        String name1 = "ABCD";

        //布尔值：是非
        boolean flag = true;

    }
}
